package LogicaDeProgramacao.desafioLista3Repeticao;

public class Questao26 {
    // Fazer um algoritmo que calcule e mostre a tabuada do 7.

    public static void main(String[] args) {
        for(int i = 0; i <= 10; i++) {
            System.out.printf("%d x %d = %d\n", 7, i, (7 * i));
        }
    }
}
